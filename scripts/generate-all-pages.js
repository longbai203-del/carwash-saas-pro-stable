// 创建页面模板
function createPageContent(moduleId, pageName) {
  const html = `<!-- ${moduleId}/${pageName}.html -->
<div class="page-container" data-page="${moduleId}-${pageName}">
  <h1>${moduleId} - ${pageName}</h1>
  <p>This page is generated by V2 scaffolding.</p>
</div>`;
  const js = `// ${moduleId}/${pageName}.js
import { ModuleV2 } from '../../../js/module-base-v2.js';

export default new ModuleV2({
  name: '${moduleId} - ${pageName}',
  routes: [
    { path: '/${moduleId}/${pageName}', component: './${pageName}.html', meta: { title: '${pageName}' } }
  ],
  init: () => {
    console.log('${moduleId} ${pageName} initialized');
    // 在此处添加业务逻辑
  }
});
`;
  return { html, js };
}

async function generateAll() {
  for (const [moduleId, pages] of Object.entries(pageMap)) {
    const moduleDir = path.join(MODULES_DIR, moduleId);
    if (!await fs.pathExists(moduleDir)) {
      console.warn(`⚠️  Module dir ${moduleId} not found, skipping.`);
      continue;
    }
    for (const page of pages) {
      const pageDir = path.join(moduleDir, page);
      await fs.ensureDir(pageDir);
      const htmlPath = path.join(pageDir, `${page}.html`);
      const jsPath = path.join(pageDir, `${page}.js`);
      if (!await fs.pathExists(htmlPath)) {
        const { html, js } = createPageContent(moduleId, page);
        await fs.writeFile(htmlPath, html);
        await fs.writeFile(jsPath, js);
        console.log(`📄 Generated ${moduleId}/${page}`);
      } else {
        console.log(`⏭️  ${moduleId}/${page} already exists, skipped.`);
      }
    }
  }
  console.log('🎉 所有子页面骨架生成完成！');
}

generateAll().catch(console.error);